/**
 * G1-TEMPLATE — the deterministic CC-plugin renderer (the "compiler").
 *
 * SSOT: docs/design/agent-skill-compile-domain-model.md §4 (the compiled artifact,
 *       3-tier progressive disclosure) + Inv II (additive projection over the Floor)
 *       + Inv VI (auth/invoke core deterministically TEMPLATED, never LLM-authored),
 *       ADR-6/ADR-8; and docs/design/cc-plugin-artifact-spec.md §1/§2/§3/§5.
 *
 * WHAT THIS IS — a pure, deterministic function (NO LLM, no network, no clock unless
 * you let it) that projects a *granted cap-set* into a ready-to-install Claude Code
 * plugin directory. Given the same (Floor + capabilityIds + agentId + code +
 * compileStamp) it emits BYTE-IDENTICAL files. It is `renderPlugin()` (returns the
 * files in memory) + `writePlugin()` (materializes them to a dir, with modes). D1-
 * ENDPOINT imports this to serve `GET /integration/<agent>`.
 *
 * THE SKELETON IT EMITS (annotated per cc-plugin-artifact-spec §5):
 *
 *   plexus@<agentId>/                       # a self-hosting one-plugin marketplace
 *   ├── .claude-plugin/
 *   │   ├── plugin.json          [T]        # identity; version = compile stamp (cache key, ADR-10)
 *   │   └── marketplace.json     [T]        # makes the dir `claude plugin marketplace add`-able
 *   ├── skills/use-plexus/
 *   │   └── SKILL.md             [P]+[T]    # tier-1 frontmatter [T] + tier-2 prose body [P]
 *   ├── bin/
 *   │   └── plexus               [S]        # tier-3: G2 engine VERBATIM (auth core, never in context)
 *   ├── install.sh               [T]        # one-command install; code rides via env->0600 scratch
 *   └── README.md                [S]        # human doc (NOT loaded as CC context — D1)
 *
 * INV VI (templated auth core, never LLM-authored) — the entire redeem->PAT->
 * handshake->token->invoke chain is `bin/plexus`, which is the COMMITTED, Floor-driven
 * G2 engine copied BYTE-FOR-BYTE (see `readEngine()`), plus the templated `install.sh`
 * whose enrollment step invokes that same engine. No prose file carries auth mechanics;
 * the SKILL body [P] is pedagogy only. Nothing here is generated by a model.
 *
 * INV III / secret hygiene — NO long-lived secret is ever written into the artifact.
 * The durable PAT never appears. The one-time enrollment code is NOT baked into any
 * emitted file; it rides the *install command string* (returned separately) in an env
 * var, which `install.sh` lands in a 0600 scratch file, redeems, then deletes.
 */

